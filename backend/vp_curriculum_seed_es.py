def seed_curriculum_es(db) -> None:
    from sqlalchemy.orm import Session
    import models

    for level, lessons in TRANSLATIONS.items():
        curriculum = db.query(models.VPCurriculum).filter(
            models.VPCurriculum.level == level
        ).first()
        if not curriculum:
            continue
        for lesson_number, outline_es in lessons.items():
            lesson = db.query(models.VPCurriculumLesson).filter(
                models.VPCurriculumLesson.curriculum_id == curriculum.id,
                models.VPCurriculumLesson.lesson_number == lesson_number,
            ).first()
            if lesson and not lesson.outline_es:
                lesson.outline_es = outline_es
        db.commit()
